package Static;

public class VariableClassName {

// Change the value of variable by class name then Print it.
	
	    static int variable = 10;

	    public static void main(String[] args) {
	        System.out.println("Original value of variable: " + variable);

	        variable = 20;

	        System.out.println("New value of variable: " + variable);
	    }
	

	}


